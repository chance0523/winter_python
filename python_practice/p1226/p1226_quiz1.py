print('홀수번째만 출력 : %s' % sumList[::2])
print('총 길이 : %d' % len(sumList))

# 퀴즈 2
'''
리스트를 정의한 후 리스트 요소를 편집한다.(변경, 삭제, 추가)
['사과', '배', '망고']
첫번째 요소 변경 후 : ['포도', '배', '망고']
마지막 위치에 요소 추가후 : ['포도', '배', '망고', '오렌지']
2번째 위치에 요소 추가후 : ['포도', '수박', '배', '망고', '오렌지']
마지막 위치 삭제 : ['포도', '수박', '배', '망고']
배 삭제 : ['포도', '수박', '망고']
'''

print('---quiz2---')
fruit = ['사과', '배', '망고']
print(fruit)
fruit[0] = '포도'
print(f'첫번째 요소 변경 후 : {fruit}')
fruit.append("오렌지")
print(f'마지막 위치에 요소 추가 후 : {fruit}')
fruit.insert(1, "수박")
print(f'2번째 위치에 요소 추가 후 : {fruit}')
fruit.pop()
print(f'마지막 위치 삭제 : {fruit}')
fruit.remove("배")
print(f'배 삭제 : {fruit}')

