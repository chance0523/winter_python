# 리스트[] 튜플() 딕셔너리{} 집합{}

# 리스트[]
fruits = ['사과', '바나나', '포도']
print(fruits)

# 빈 리스트
movieList = []
print(movieList)

# 서로 다른 데이터형으로 리스트 정의
myList = [True, 100, '홍길동']
print(f'myList = {myList}')

# 리스트 인덱싱 / 슬라이싱
print(fruits[0], fruits[-1])
# 리스트이름[start:end:step])
print(fruits[:1])
print(fruits[0:1])
print(fruits[0::2])
print(fruits[::])
